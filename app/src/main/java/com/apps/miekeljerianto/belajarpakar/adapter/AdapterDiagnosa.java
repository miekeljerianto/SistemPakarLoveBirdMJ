package com.apps.miekeljerianto.belajarpakar.adapter;

public class AdapterDiagnosa {
    String ID;
    String diagnosa;
    String penyebab;
    String solousi;
    String kembali;
    String tidak;
    String ya;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getDiagnosa() {
        return diagnosa;
    }

    public void setDiagnosa(String diagnosa) {
        this.diagnosa = diagnosa;
    }

    public String getPenyebab() {
        return penyebab;
    }

    public void setPenyebab(String penyebab) {
        this.penyebab = penyebab;
    }

    public String getSolousi() {
        return solousi;
    }

    public void setSolousi(String solousi) {
        this.solousi = solousi;
    }

    public String getKembali() {
        return kembali;
    }

    public void setKembali(String kembali) {
        this.kembali = kembali;
    }

    public String getTidak() {
        return tidak;
    }

    public void setTidak(String tidak) {
        this.tidak = tidak;
    }

    public String getYa() {
        return ya;
    }

    public void setYa(String ya) {
        this.ya = ya;
    }
}
